package std1;

public class StudentData 
{
	private String studentName;
	private int math;
	private int phy;
	private int bio;
	private int total;
	
	
	public String getStudentName() 
	{
		return studentName;
	}
	
	
	public void setStudentName(String sn) 
	{
		studentName = sn;
	}
	
	
	public int getMath() 
	{
		return math;
	}
	
	
	public void setMath(int m1) 
	{
		math = m1;
	}
	
	
	public int getPhy() 
	{
		return phy;
	}
	
	
	public void setPhy(int p1) 
	{
		phy = p1;
	}
	
	
	public int getBio() 
	{
		return bio;
	}
	
	
	public void setBio(int b1) 
	{
		bio = b1;
	}
	
	
	public int getTotal() 
	{
		return total;
	}
	
	
	public void setTotal(int t1) 
	{
		total = t1;
	}
	
	

}
