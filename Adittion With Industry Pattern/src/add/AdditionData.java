package add;

public class AdditionData 
{
	
	
	private double firstvalue;
	private double secondvalue;
	private double result;
	
	//read the value
	public double getFirstvalue() 
	{
		return firstvalue;
	}
	
	//storing the value inside the variable
	public void setFirstvalue(double f1) 
	{
		firstvalue = f1;
	}
	
	
	public double getSecondvalue() 
	{
		return secondvalue;
	}
	
	
	public void setSecondvalue(double s1) 
	{
		secondvalue = s1;
	}
	
	
	public double getResult() 
	{
		return result;
	}
	
	
	public void setResult(double r1) 
	{
		result = r1;
	}
	
	

}
